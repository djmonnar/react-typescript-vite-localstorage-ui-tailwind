function createDefaultMatrix() {
  const overrides: Record<string, number> = {
    'atk_physical:def_heavy': 0.8,
    'atk_physical:def_spirit': 0.6,
    'atk_magic:def_spirit': 1.4,
    'atk_magic:def_machine': 0.8,
    'atk_pierce:def_light': 1.25,
    'atk_pierce:def_heavy': 0.85,
    'atk_explosive:def_heavy': 1.3,
    'atk_explosive:def_light': 0.85,
    'atk_explosive:def_machine': 1.15,
  };

  return attackTypes.flatMap((attackType) =>
    defenseTypes.map((defenseType) => ({
      attackTypeId: attackType.id,
      defenseTypeId: defenseType.id,
      multiplier: overrides[`${attackType.id}:${defenseType.id}`] ?? 1,
    })),
  );
}

